<p>Each program should use the same step-by-step spigot algorithm to calculate digits of Pi.</p>

<p>Each program should</p>
<ul>
  <li>calculate the first N digits of Pi</li>
  <li>print the digits 10-to-a-line, with the running total of digits calculated</li>
</ul>

<p>Programs should adapt the <strong>step-by-step</strong> algorithm given on pages 4,6 &amp; 7 of <a href="http://web.comlab.ox.ac.uk/oucl/work/jeremy.gibbons/publications/spigot.pdf">Unbounded Spigot Algorithms for the Digits&nbsp;of&nbsp;Pi</a> (156KB pdf). (<strong>Not</strong> the deliberately obscure version given on page 2.)</p>

<p>Correct output N = 27 is:</p>
<pre>
3141592653	 :10
5897932384	 :20
6264338    	 :27
</pre>
