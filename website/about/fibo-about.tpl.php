<p>Each program should calculate the Fibonacci function using the same na&#239ve recursive-algorithm
<pre>
F(n)
  n = 0     = 0
  n = 1     = 1
  otherwise = F(n-2) + F(n-1)
</pre>
</p><br/>

<p>Calculate F(N). Correct output N = 32 is:
<pre>
2178309
</pre>
