<h5>The Weighted Geometric Mean</h5>
<p>For each benchmark, the <em>best measurement</em> <strong>B</strong> is the lowest non-zero measurement. For each language implementation, the measurement <strong>L</strong> for the language implementation is converted to the <em>ratio</em> <strong>L/B</strong></p>
<p>The ratio can be adjusted by a measurement multiplier (make CPU time or memory use or
lines-of-code more or less important) and by a benchmark weight (make particular benchmarks more or less important).</p>
<p>For each language implementation, we report the weighted geometric mean of those <strong>L/B</strong> ratios.</p>





